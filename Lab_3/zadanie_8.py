# Zadanie 8: Operacje na łańcuchach
tekst = "Python jest super"
print(tekst[0])  # Zerowy znak
print(tekst[-1])  # Ostatni znak
print(tekst[::2])  # Co drugi znak
print(tekst[1::3])  # Co trzeci znak od pierwszego
print(tekst[10:])  # Od dziesiątego do końca
print(tekst[::-1])  # Od końca do początku